public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");
        // цифра
        int digit = 3;
        System.out.println("цифра = " + digit);

        // сумма чисел
        int numbersSum = 25;
        System.out.println("сумма чисел = " + numbersSum);

        // произведение цифр
        int numbersProduct = 78;
        System.out.println("произведение цифр = " + numbersProduct);

        // максимальное число
        int maxNumber = 32;
        System.out.println("максимальное число = " + maxNumber);

        // количество десятков
        int tensCount = 6;
        System.out.println("количество десятков = " + tensCount);

        // вес собаки
        float dogWeight = 3.6F;
        System.out.println("вес собаки = " + dogWeight);

        // исходное число
        int originalNumber = 5892;
        System.out.println("исходное число = " + originalNumber);

        // процент по вкладу
        double depositInterest = 15e-2;
        System.out.println("процент по вкладу = " + depositInterest);

        // (переменная хранит символ) &
        char ampersand = '&';
        System.out.println("переменная хранит символ & = " + ampersand);

        // код ошибки
        int errorCode = 404;
        System.out.println("код ошибки = " + errorCode);

        // тип сообщения
        String messageType = "Urgent";
        System.out.println("тип сообщения = " + messageType);

        // число нулей
        int zerosCount = 5;
        System.out.println("число нулей = " + zerosCount);

        // уникальное число
        int uniqueNumber = 35678991;
        System.out.println("уникальное число = " + uniqueNumber);

        // случайное число
        int randomNumber = 885219;
        System.out.println("случайное число = " + randomNumber);

        // математическое выражение
        String mathExpression = "Multiplication";
        System.out.println("математическое выражение = " + mathExpression);

        // счет в игре
        String gameScore = " 83 : 72";
        System.out.println("счет в игре = " + gameScore);

        // максимальная длина
        int maxLength = 2500;
        System.out.println("максимальная длина = " + maxLength);

        // пункт меню
        char menuItem = 'a';
        System.out.println("пункт меню = " + menuItem);

        // стоимость кофе
        float coffeePrice = 232e-2F;
        System.out.println("стоимость кофе = " + coffeePrice);

        // дата начала
        String startDate = "19.05.2021";
        System.out.println("дата начала = " + startDate);

        // окончание диапазона
        int endOfRange = 27556900;
        System.out.println("окончание диапазона = " + endOfRange);

        // полное имя работника месяца
        String monthWorkerFullName = "John Doe";
        System.out.println("полное имя работника месяца = " + monthWorkerFullName);

        // заголовок электронной книги
        String ebookTitle = "War of the Worlds";
        System.out.println("заголовок электронной книги = " + ebookTitle);

        // размер
        String size = "XL";
        System.out.println("размер = " + size);

        // вместимость
        int capacity = 25869;
        System.out.println("вместимость = " + capacity);

        // счетчик
        int counter = 0;
        System.out.println("счетчик = " + counter);

        // путь до файла
        String filePath = "C:\\\\StartJava\\Lesson_1\\";
        System.out.println("путь до файла = " + filePath);

        // количество чисел в каждой строке
        int amountOfNumbersPerString = 23;
        System.out.println("количество чисел в каждой строке = " + amountOfNumbersPerString);

        System.out.println("\n2. boolean-переменные");
        // сотни равны?
        boolean hasEqualHundreds = false;
        System.out.println("сотни равны? = " + hasEqualHundreds);

        // компьютер включен?
        boolean isComputerTurnedOn = false;
        System.out.println("компьютер включен? = " + isComputerTurnedOn);

        // есть равные цифры?
        boolean hasEqualDigits = true;
        System.out.println("есть равные цифры? = " + hasEqualDigits);

        // создано?
        boolean isCreated = true;
        System.out.println("создано? = " + isCreated);

        // пустое?
        boolean isEmpty = false;
        System.out.println("пустое? = " + isEmpty);

        // активное?
        boolean isActive = true;
        System.out.println("активное? = " + isActive);

        // новое?
        boolean isNew = false;
        System.out.println("новое? = " + isNew);

        // электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("электронная почта действительная? = " + isValidEmail);

        // имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println("имеются уникальные строки? = " + hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");
        // старый universally unique identifier
        int oldUuid = 4321;
        System.out.println("старый universally unique identifier = " + oldUuid);

        // производитель оперативной памяти
        String ramManufacturer = "Samsung";
        System.out.println("производитель оперативной памяти = " + ramManufacturer);

        // емкость жесткого диска
        long hddCapacity = 326546897L;
        System.out.println("емкость жесткого диска = " + hddCapacity);

        // протокол передачи гипертекста
        int http = 120;
        System.out.println("протокол передачи гипертекста = " + http);

        // сокращенный uniform resource locator
        String shortUrl = "www.google.com";
        System.out.println("сокращенный uniform resource locator = " + shortUrl);

        // новый идентификатор клиента
        int newCid = 4578;
        System.out.println("новый идентификатор клиента = " + newCid);

        // кодировка american standard code for information interchange
        int ascii = 6;
        System.out.println("кодировка american standard code for information interchange = " + ascii);
    }
}